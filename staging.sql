CREATE OR REPLACE TABLE STAGING.CUSTOMERS ( 
  CUSTOMER_ID VARCHAR PRIMARY KEY, 
  FULL_NAME VARCHAR, 
  EMAIL VARCHAR, 
  STATE VARCHAR, 
  CREATED_AT TIMESTAMP 
); 

CREATE OR REPLACE TABLE STAGING.PRODUCTS ( 
  SKU VARCHAR PRIMARY KEY, 
  PRODUCT VARCHAR, 
  CATEGORY VARCHAR, 
  PRICE NUMBER(12,2) 
); 

CREATE OR REPLACE TABLE STAGING.ORDERS ( 
  ORDER_ID VARCHAR PRIMARY KEY, 
  CUSTOMER_ID VARCHAR, 
  ORDER_TS TIMESTAMP,
  TOTAL_AMOUNT NUMBER(12,2),
  TAX_AMOUNT NUMBER(12,2)
);

CREATE OR REPLACE TABLE STAGING.ORDER_ITEMS ( 
  ORDER_ID VARCHAR, 
  ORDER_ITEM INT, 
  SKU VARCHAR, 
  QUANTITY INT, 
  UNIT_PRICE NUMBER(12,2),
  PRIMARY KEY (ORDER_ID, ORDER_ITEM)
);

